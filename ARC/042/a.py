#solve
def solve():
    n, m = LI()
    a = IR_(m)
    d = [True] * n
    ans = []
    for ai in a[::-1]:
        if d[ai]:
            ans.append(ai+1)
            d[ai] &= False
    for i in range(n):
        if d[i]:
            ans.append(i+1)
    print("\n".join(map(str, ans)))
    return


