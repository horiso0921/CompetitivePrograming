def solve():
    n,m = LI()
    ab = [[] for i in range(n+1)]
    for _ in range(m):
        a,b = LI()
        heappush(ab[a], -b)
    dp = SegTree([0] * (n+1), segfunc, ide_ele)
    for i in range(n+1):
        while ab[i]:
            b = -heappop(ab[i])
            x1 = dp.query(0, b-1)
            x2 = dp.query(0, b)
            if x1 == x2:
                dp.update(b - 1, x1 + 1)

    print(dp.query(0, n))        
    return


